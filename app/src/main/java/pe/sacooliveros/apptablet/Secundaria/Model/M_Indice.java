package pe.sacooliveros.apptablet.Secundaria.Model;

public class M_Indice {

    String descripcion;
    String pagina;
    String pagina_doc;



    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPagina() {
        return pagina;
    }

    public void setPagina(String pagina) {
        this.pagina = pagina;
    }

    public String getPagina_doc() {
        return pagina_doc;
    }

    public void setPagina_doc(String pagina_doc) {
        this.pagina_doc = pagina_doc;
    }
}
